#=======================================================================================
# Import variables from arguments.
#=======================================================================================
import sys

ADMIN_HOST = sys.argv[1]
ADMIN_PORT = sys.argv[2]
ADMIN_PASSWORD = sys.argv[3]
SERVER = sys.argv[4]
CLUSTER = sys.argv[5]

#=======================================================================================
# Connect to running domain.
#=======================================================================================
connect('weblogic',ADMIN_PASSWORD,'t3://' + ADMIN_HOST + ':' + ADMIN_PORT)

#=======================================================================================
# Create cluster.
#=======================================================================================
edit()
startEdit()

print('Assigning server ' + SERVER + ' to cluster ' + CLUSTER)
cd('/Servers/' + SERVER)
cmo.setCluster(getMBean('/Clusters/' + CLUSTER))

activate()

#=======================================================================================
# Exit WLST.
#=======================================================================================
exit('y')
