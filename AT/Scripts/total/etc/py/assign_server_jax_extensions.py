#=======================================================================================
# Import variables from arguments.
#=======================================================================================
import sys

DOMAIN_DIR = sys.argv[1]
SERVER = sys.argv[2]

readDomain(DOMAIN_DIR)
#=======================================================================================
# Configure the JMS module as a Uniform Distributed Destination
#=======================================================================================
setDistDestType('WseeJaxwsJmsModule', 'UDD')

#=======================================================================================
# Target WseeJaxwsJmsModule to the server
#=======================================================================================
assign('JMSSystemResource', 'WseeJaxwsJmsModule', 'Target', SERVER)

#=======================================================================================
# Import the wls_webservice_complete_update_utils.py script
#=======================================================================================
sys.path.append(DOMAIN_DIR)
import wls_webservice_complete_update_utils as update
update.doWseeFixup(globals())

#=======================================================================================
# Update domain information
#=======================================================================================
updateDomain()
closeDomain()

#=======================================================================================
# Exit WLST.
#=======================================================================================
exit('y')
